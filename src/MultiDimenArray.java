



class MultiDimenArray
{  
public static void main(String args[]){  
  
//declaring and initializing 2D array  
int arr[][]={{12,42,63},{12,44,65},{24,44,55}};  
  
//printing 2D array  s
for(int i=0;i<3;i++){  
 for(int j=0;j<3;j++){  
   System.out.print(arr[i][j]+" ");  
 }  
 System.out.println(); 
}  
  
}} 