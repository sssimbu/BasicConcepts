package oops_concept;

public class EncapsulationGetAndSet {

	private int a;
	int b;
	String name;
	float c;

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public String getName() {
		return name;
	}

	void setName(String name) {
		this.name = name;
	}

	public float getC() {
		return c;
	}

	public void setC(float c) {
		this.c = c;
	}

}
